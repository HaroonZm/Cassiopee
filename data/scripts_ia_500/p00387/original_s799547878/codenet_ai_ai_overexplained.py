# 標準入力からデータを取得する処理です。  
# input()関数はユーザーがキーボードから入力した文字列を読み込みます。  
# この場合、スペースで区切られた2つの整数値が与えられることを期待しています。  
# 例えば、「3 10」と入力すると、"3"と"10"という文字列が取得されます。  
# split()メソッドは文字列を空白文字（デフォルト）で分割し、リストに変換します。  
# したがって、"3 10".split()は["3","10"]というリストになります。  
# map()関数はリストの各要素に対してint関数を適用し、文字列を整数に変換します。  
# 最終的にa,bには整数型の2つの値が格納されます。  
a, b = map(int, input().split())  

# ここから条件分岐を開始します。  
# b % aという式はbをaで割った余りを計算します。  
# 余りが0であれば、bはaの整数倍であることを意味します。  
# 例えば、10を5で割ると余りは0で、これは10が5の倍数であることを示します。  
if b % a == 0:  
    # 条件が成り立った場合、bをaで割った整数商を表示します。  
    # //演算子は整数除算を行い、小数点以下を切り捨てます。  
    # 例えば、10 // 3は3になります。  
    print(b // a)

# もしbがaの倍数でなければ、elseブロックが実行されます。  
else:  
    # bをaで割った整数商に1を足して出力します。  
    # これはbをaで割った結果を切り上げる処理に相当します。  
    # 例えば、10 // 3は3ですが、切り上げると4になります。  
    print(b // a + 1)