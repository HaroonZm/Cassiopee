# 必要な机の数と机1つあたりの椅子の数を入力として受け取り、
# その積を計算して必要な椅子の総数を出力するプログラム

# 入力例: "20 2"
# 出力例: 40

# 処理手順：
# 1. 入力文字列を受け取る
# 2. スペースで分割し、机の数 d と椅子の数 c に変換
# 3. d と c の積を計算
# 4. 結果を出力

def main():
    # 1行の入力を受け取る
    line = input()
    # スペースで分割し、整数に変換
    d, c = map(int, line.split())
    # 机の数と椅子の数の積を計算
    total_chairs = d * c
    # 必要な椅子の総数を出力
    print(total_chairs)

if __name__ == "__main__":
    main()