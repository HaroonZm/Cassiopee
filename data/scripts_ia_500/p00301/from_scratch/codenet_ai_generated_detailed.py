w = int(input())

# 対称3進数変換
# 3進数の各桁について、0, 1, 2 の数字を、0, +1, -1 に変換するイメージ
# 具体的には、余りを3で見て、
# 0 のとき： '0' (置かない)
# 1 のとき： '+' (おもりを反対皿に置く)
# 2 のとき： '-' (おもりを量りたいものの皿に置く、繰り上がりが発生)
# 2 は -1 と考え、繰り上がりで次の桁に +1 を足す
# これにより、唯一の対称3進数表現が得られる

res = []
n = w
while n > 0:
    r = n % 3
    n //= 3
    if r == 0:
        res.append('0')
    elif r == 1:
        res.append('+')
    else:  # r == 2 は -1 として繰り上がり
        res.append('-')
        n += 1

# 桁は下位桁から格納しているので、左右を逆にして表示する
# 左端が0になってはいけないので、余計な0は存在しない
print(''.join(res[::-1]))