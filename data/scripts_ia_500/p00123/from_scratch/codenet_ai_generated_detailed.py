# スピードスケートバッジテスト判定プログラム

# 各級の規定タイムを秒単位で定義する
# 500Mと1000Mの両方のタイムをクリアする必要がある

def main():
    # バッジテストの基準タイムを辞書で管理（単位は秒）
    # 1000Mの分・秒を秒に換算して登録
    badge_standards = [
        ('AAA', 35.50, 71.00),   # 1分11秒00 -> 71.00秒
        ('AA',  37.50, 77.00),   # 1分17秒00 -> 77.00秒
        ('A',   40.00, 83.00),   # 1分23秒00 -> 83.00秒
        ('B',   43.00, 89.00),   # 1分29秒00 -> 89.00秒
        ('C',   50.00, 105.00),  # 1分45秒00 -> 105.00秒
        ('D',   55.00, 116.00),  # 1分56秒00 -> 116.00秒
        ('E',   70.00, 148.00),  # 1分10秒00 -> 70.00秒, 2分28秒00 -> 148.00秒
    ]

    import sys

    for line in sys.stdin:
        line = line.strip()
        if not line:
            continue
        t1_str, t2_str = line.split()
        t1 = float(t1_str)  # 500Mタイム（秒）
        t2 = float(t2_str)  # 1000Mタイム（秒）

        # どの級に該当するか判定
        result = 'NA'  # 初期値はNA
        for grade, time_500m, time_1000m in badge_standards:
            # 500Mタイム、1000Mタイムともに基準未満なら、その級に合格
            if t1 < time_500m and t2 < time_1000m:
                result = grade
                break  # 上位級から順に判定しているため、最初に合格した級が最上位

        print(result)

if __name__ == '__main__':
    main()