while True:
    b, r, g, c, s, t = map(int, input().split())
    if b == 0 and r == 0 and g == 0 and c == 0 and s == 0 and t == 0:
        break

    # 初期メダル数
    medals = 100

    # ビッグボーナスゲーム数とレギュラーボーナスゲーム数の合計
    bonus_games = b * 5 + r * 3

    # ①ノーマルゲームでのメダル投入とボーナス等の取得
    # 通常ゲーム数 s のうち、b+r ボーナスゲームを除いたものが通常ゲーム数(メダル投入3枚)
    # 入力のsは通常ゲーム数なので、普通のゲームはsゲーム
    # ただしゲームはt（全ゲーム数）=通常s + ボーナスゲーム数 でボーナスは b*5 + r*3
    # 問題文より、通常ゲームはメダル3枚ずつ投入し、スター揃い時は無料ゲーム（メダル投入なし）

    # スターs回だけ無料ゲームなので、実際に3枚メダル投入した回数は s - c (cは無料ゲーム)
    normal_medal_in = (s - c) * 3
    medals -= normal_medal_in

    # スターの揃いは無料ゲームなのでメダルは増減なし

    # チェリー揃いについて特に記述がないので、メダル増減なしと考える（問題文ではチェリー揃いは言及なし）

    # ブドウ揃いg回は通常ゲームの中で自動的に揃ったときの回数
    # ボーナスゲーム中は必ずブドウ揃い（2枚投入で15枚獲得）、しかしgは通常ゲームでのブドウの回数なので別枠
    # ボーナスゲーム中は自動的に揃うので合計のメダル獲得量に含める

    # ② 通常ゲームでのブドウ揃いによるメダル獲得
    medals += g * 15

    # ③ ボーナスゲーム中のメダル投入と獲得
    # ボーナスゲームは b*5 + r*3 ゲームある
    # 各ゲーム2枚投入し、必ずブドウ3つ揃いで15メダル獲得
    bonus_game_count = b * 5 + r * 3
    medals -= bonus_game_count * 2  # ボーナスゲーム中のメダル投入計
    medals += bonus_game_count * 15 # ボーナスゲーム中のメダル獲得計

    # ④ ビッグボーナス、レギュラーボーナスで揃った図柄による追加メダル
    # ビッグボーナスの3つ7揃い時に得られるメダルは、通常ゲームで揃ったときのみ与えられるらしいが明示されていない
    # 問題の説明によると図柄揃いでメダルが得られ、7の3つ揃いでビッグボーナス開始、バナースゲーム行う
    # しかし獲得メダル数についてはビッグボーナスゲーム中は2投入⇒15枚ゲットとだけ書かれている
    # 通常ゲーム中の7揃い報酬は書かれていないため、ビッグボーナス開始による増加はボーナスゲームでのメダル獲得に含まれる

    # ⑤ 総ゲーム数 t はボーナスゲームも含むがメダル減少なしなので無視

    # 最終メダル数を出力
    print(medals)