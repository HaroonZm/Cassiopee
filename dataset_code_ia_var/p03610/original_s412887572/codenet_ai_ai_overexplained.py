# まず、ユーザーから1行の入力を受け取り、それを1文字ずつのリストに変換します
# input()関数は、標準入力からデータを文字列として取得します
# list()関数に文字列を渡すと、各文字を要素に持つリストが作られます
strings = list(input())

# for文を使って、0から(stringsの長さ-1)までの整数を順番に取り出します
# range()は指定した範囲の整数（この場合は0からlen(strings)-1まで）を生成するイテレータです
# len(strings)はリストstringsの要素数、つまり入力された文字数を返します
for number in range(len(strings)):
    # numberは各反復で0,1,2,...として増えます。リストのインデックスとして利用されます。

    # if文を使ってnumberが偶数（0,2,4,...）かどうか調べます
    # %は剰余演算子で、number % 2はnumberを2で割った余りを求めます
    # number % 2 == 0 で、numberが偶数であることを判定します
    if number % 2 == 0:
        # 条件がTrue（つまりnumberが偶数）のときだけ、対応する文字を出力します

        # print()関数はオブジェクトを出力しますが、通常は出力後に改行します
        # end="" を指定することで、出力後に改行せず、代わりに空文字（""）を出力します
        # これにより、複数回print()しても全ての文字が同じ行に連続して表示されます
        print(strings[number], end="")
        # strings[number]は、現在の位置numberの文字を取得します

# 入力が例えば 'abcdefg' ならば、偶数番目（0,2,4,6）の文字"aceg"が出力されます