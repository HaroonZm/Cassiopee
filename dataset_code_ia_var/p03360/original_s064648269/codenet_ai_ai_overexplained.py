# このプログラムは3つの整数の入力と、1つの整数Kの入力を受け取ります。
# これらを使って特定の計算を行い、その結果を出力します。

# input() 関数はユーザーから入力を受け取る標準の方法です。
# split() メソッドは入力された文字列を空白文字で分割し、リストにします。
# map(int, ...) はリストの各要素（文字列）をint型（整数型）に変換します。
# こうして3つの整数を同時に入力し、それぞれA, B, Cという変数に代入します。
A, B, C = map(int, input().split())

# ユーザーからさらに1つ整数を入力し、その値をKという変数に代入します。
K = int(input())

# max(A, B, C) はA, B, Cの中で最も大きい値を返します。
# 2 ** Kは、2をK乗した値を求めます（2をK回掛ける、指数演算）。
# (2 ** K - 1) で、(2^K)から1を引いた値になります。
# max(A, B, C) * (2 ** K - 1) で、最大値を(2**K-1)回加算する操作と等価です。

# A + B + Cは、A, B, Cの合計です。
# これに最大値の「追加分」を加算して、最終結果のansとします。
ans = A + B + C + max(A, B, C) * (2 ** K - 1)

# print(ans) で計算された結果(ans)をコンソールに出力します。
print(ans)