# La fonction print() en Python est utilisée pour afficher des informations à la sortie standard, généralement à l'écran.
# Ici, nous utilisons la fonction print pour afficher une chaîne de caractères.
# Une chaîne de caractères (string) est une séquence de caractères encadrée par des guillemets.
# Les guillemets peuvent être simples (' '), doubles (" "), ou triples (''' ''', """ """).
# Dans cet exemple, nous utilisons des doubles guillemets pour définir la chaîne de caractères à afficher.
# La chaîne de caractères que nous voulons afficher est "square1001".
# Lorsque le programme atteint la ligne suivante, il exécutera la fonction print, qui affichera le texte à l'utilisateur.
print("square1001")