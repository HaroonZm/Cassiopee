# La fonction print() est une fonction intégrée du langage Python.
# Elle sert à afficher des informations à l'écran, plus précisément dans la console ou le terminal.
# Les informations à afficher sont passées à la fonction print() entre parenthèses sous la forme d'arguments.
# Dans ce cas, l'argument est la chaîne de caractères 'square1001'.
# Une chaîne de caractères, aussi appelée "string", est une séquence de caractères encadrée par des guillemets simples ('') ou doubles ("").
# Ici, la chaîne contient la combinaison de lettres "s", "q", "u", "a", "r", "e", le chiffre "1", "0", "0" et "1".
# Lorsque ce code est exécuté, Python traite la fonction print() et affiche exactement le contenu de la chaîne à l'utilisateur.
# Aucune valeur n'est retournée par la fonction print(), qui sert uniquement à afficher des données.
print('square1001')