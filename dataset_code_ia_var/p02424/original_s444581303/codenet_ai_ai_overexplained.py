# このプログラムは、2つの整数を標準入力から受け取り、
# ビットごとのAND演算・OR演算・XOR演算を行い、その結果を32ビットの2進数で表示します。

# input() 関数を使って1行の文字列入力を受け取ります。
# .split() 関数で空白ごとに分割して、リスト（配列）にします。
# map(int, ...) でリスト内の各要素を整数（int型）に変換します。
# a, b は2つの整数です。
a, b = map(int, input().split())

# a と b のビットごとのAND（&）を行います。
# 例えば 1101 & 1011 = 1001 のように、各ビットで両方とも1の場合だけ1になります。
# "{:032b}".format(...) で、計算結果を32桁の2進数（b: binary）文字列に変換し、0埋めします。
# 例えば10なら '00000000000000000000000000001010' という表示になります。
print("{:032b}".format(a & b))

# a と b のビットごとのOR（|）を行います。
# 両方または一方が1なら1になります。
print("{:032b}".format(a | b))

# a と b のビットごとのXOR（^）を行います。
# どちらか一方が1の場合だけ1になります（排他的論理和）。
print("{:032b}".format(a ^ b))