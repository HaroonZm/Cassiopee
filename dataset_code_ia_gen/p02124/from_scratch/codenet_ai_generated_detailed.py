# 入力として与えられる経過年数xは100の非負整数倍であり、0 <= x <= 10000が保証されている。
# 初代人工知能のIDは "ai1333" である。
# 100年ごとに新たな人工知能が誕生し、IDは前のIDの末尾に '3' を連結したものとなる。
# したがって、x年後の人工知能のIDは、
# 100年周期の数 n = x // 100 に応じて、
# "ai1333" に '3' を n 回連結した文字列である。

def main():
    x = int(input())

    # 100年ごとの周期数を計算
    n = x // 100

    # 基本ID
    base_id = "ai1333"

    # '3' を n 回連結
    result = base_id + "3" * n

    # 出力
    print(result)

if __name__ == "__main__":
    main()