# 入力値を取得
A, B = map(int, input().split())

# 問題の図形の説明を整理すると：
# 1. 直角三角形の直角の一辺がx軸上にあり長さA
# 2. もう一辺がy軸上にあり長さB
# 3. この三角形をx軸周りに回転 → 回転体ができる（このときの断面は高さBの扇形）
# 4. さらに得られた回転体をy軸周りに回転 → 2回回転の体積を求める

# Step1: x軸周りの回転体の体積
# 直角三角形の辺がx軸とy軸にそれぞれ沿っているので、
# x軸周りに回転するとき、回転体の断面積は円になる。
# 三角形のy座標はy = (B/A)*x（x方向の長さAに対して）
# 断面の半径はy座標なので、断面積はπ*(y^2) = π * (B^2/A^2) * x^2
# xは0からAまで動く。

import math

# x軸周りの回転体の体積は積分で求められる:
# V1 = ∫[0..A] π * (B^2/A^2) * x^2 dx = π * (B^2/A^2) * ∫ x^2 dx
#     = π * (B^2/A^2) * (A^3 / 3) = π * B^2 * A / 3

V1 = math.pi * B * B * A / 3

# Step2: この回転体をy軸周りに回転させる。
# x軸周りの回転体はy軸方向に座標が変わるので、正確な体積を計算するには積分を行う必要がある。
# ここで、x軸周りに回転した図形はベッセル型の3D形状。
# しかし積分で求めるのは複雑なので、以下の方法で求める。

# 回転体の形状は、x軸周り回転体の断面形状は楕円断面で、
# y軸周りに回転した体積は、x軸周りの回転体の断面のx座標を回転半径としての回転体の体積を計算することになる。

# 具体的には、x軸周り回転体の形状をz,x,y座標で表現すると、
# - xは0からAまで
# - y^2 + z^2 = (B/A)^2 * x^2
# つまり楕円の外周が断面である。

# この形状をy軸周りに回転させるとき、
# y軸周りの回転は、x-z平面の形状を回転させることに等しい。

# Step2の体積は、xをパラメータとして、断面円の半径(回転軸からの距離）を使い積分で計算する。

# x軸周り回転体の断面（y,z平面）の半径は (B/A)*x
# この回転体をy軸周りに回転させると、
# y軸周りの半径は sqrt(x^2 + z^2) だが、zは断面方向。

# しかし断面はy^2 + z^2 = (B/A)^2 x^2 なので、
# 半径r = sqrt(x^2 + z^2)
# zは円周方向、z^2 は任意の円周方向に合わせて積分するので総合的な半径の二乗は、
# まず断面の半径は (B/A)*x
# 点の座標は (x, y, z)
# ここでy軸回りに回転とは、x,zをx-z平面の点がy軸周りに回転するので、
# y軸まわり回転体の体積は∫ π * R(x)^2 dx
# ただし、R(x)はx-z平面での半径。
# x-z平面内の断面は円 (y,z)の円の擬似半径で、この円のx成分はx座標で固定なので
# 回転半径R(x) = sqrt(x^2 + z^2)
# zは円周方向なので平均すると
# R_max(x) = sqrt(x^2 + ((B/A)*x)^2 ) = x * sqrt(1 + (B/A)^2 )

# しかし体積は断面積の積分なので
# y軸回りの体積 = ∫[0..(B/A)*A] π * r(y)^2 dy
# ここでr(y)はx軸回り回転体の断面のx軸方向の長さ

# 少し複雑なので、極座標変換する方法を使う。

# 直接的な計算は次の式が導かれる：
# 直角三角形を2回回転させて得られる体積は
# (1/6) * π^2 * A^2 * B^2
# これは回転体の公式と2回の回転体積からわかる。

# 証明（導出）:
# - x軸回り回転体の体積 = π * B^2 * A / 3 (先に計算済み)
# - これをy軸回りに回転体として考えると球面座標で計算し
#   2回の回転によって体積は (1/6)*π^2*A^2*B^2 となる。

# 与えられたサンプル値で確認：
# A=1, B=2 のとき
# V = (1/6)*π^2*1^2*2^2 = (1/6)*π^2*4 ≈ (4/6)*π^2 ≈ 0.6666*9.8696=6.5796ではないので間違い。

# サンプルの出力は33.510322なので違う。

# よって正確な計算が必要。

# 以下の方法で正確に計算する。

# パラメータ t を使って三角形のx,y座標をパラメータ表示
# x = s (0 <= s <= A)
# y = (B/A)*s
# x軸周りに回転してできる断面はy,z平面での半径r_yz = y = (B/A)*s
# この図形をy軸周りに回転するとき、その体積は
# V = ∫[s=0..A] 2π * x * 垂直断面積 ds
# ここで
# - perpendicular断面積はx軸回りの断面で半径 r_yz = (B/A)*s の円だから π*(r_yz)^2
# - 回転軸(y軸)からの距離はx=s

# よって
# V = ∫ 2π * s * π * ( (B/A)^2 * s^2 ) ds = 2π^2 * (B^2 / A^2) ∫ s^3 ds (0~A)
# = 2π^2 * (B^2 / A^2) * (A^4 /4 )
# = 2π^2 * (B^2 / A^2) * A^4 /4 = (2π^2 /4) * B^2 * A^2
# = (π^2 / 2) * A^2 * B^2

# これで計算した体積を出力する。

V = (math.pi ** 2 / 2) * (A ** 2) * (B ** 2)

# 出力。誤差は0.000001まで許容されるのでfloatでOK
print(V)