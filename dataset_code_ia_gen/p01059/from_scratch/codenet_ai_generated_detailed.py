# 入力の受け取り
n, m = map(int, input().split())  # アイドルの人数nと秘密を知っている人数m
a = list(map(int, input().split()))  # 秘密を知っているアイドルの番号リスト（昇順）

# アプローチ:
# 情報は隣接するアイドル間で1単位の時間で伝わる。
# 複数の情報源がある場合は、
# 各アイドルが最も近い秘密保持者から情報を得るまでの時間は、
# そのアイドル番号と秘密保持者番号の差の最小値。
# 全員が情報を得るまでの最小時間は、
# 全アイドルについてその最小距離の最大値となる。
#
# 効率的な方法としては、左端から右端まで各アイドルへの最短距離を考え、
# かつ右端から左端への最短距離も考慮するために、
# 秘密保持者の位置から左右に距離を拡げながら距離を計算する。

# 距離配列を大きな値で初期化
INF = 10**9
dist = [INF] * (n + 1)

# 秘密を知っているアイドルの距離は0
for pos in a:
    dist[pos] = 0

# 左から右へ伝搬
for i in range(1, n):
    dist[i+1] = min(dist[i+1], dist[i] + 1)

# 右から左へ伝搬
for i in range(n, 1, -1):
    dist[i-1] = min(dist[i-1], dist[i] + 1)

# 最も距離が大きいものを答えとする
print(max(dist[1:]))