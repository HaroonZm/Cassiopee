# 探したい文字列と指輪の情報を受け取り、探したい文字列が指輪に刻印された文字列のどの回転にも含まれるかを判定して、
# 含まれる指輪の数を数えるプログラム

# 入力
search = input().strip()  # 探したい文字列（大文字アルファベット1〜10文字）
N = int(input().strip())  # 指輪の個数（1〜100）

count = 0  # 探したい文字列を含む指輪の数を数える変数

for _ in range(N):
    ring = input().strip()  # 指輪に刻印された文字列（10文字）

    # 指輪の文字列は最初と最後がつながっているとみなせるため、
    # 文字列を2回連結して、その中に探したい文字列が含まれるかを判定すればよい
    doubled_ring = ring * 2

    # 探したい文字列が回転文字列内に含まれているか？
    if search in doubled_ring:
        count += 1  # 含まれていればカウントアップ

# 結果を出力
print(count)